package com.codecool.mightytextadventure.data;

public class SubArea {

    public SubArea(String name, String scene) {
        this.name = name;
        this.scene = scene;
    }

    public String getName() {
        return name;
    }

    public String getScene() {
        return scene;
    }

    private String name;
    private String scene;
}
